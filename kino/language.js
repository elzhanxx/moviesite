  function changeLang(lng) {
    var elements = document.querySelectorAll("[data-tr]");
    for (var i = 0; i < elements.length; i++) {
        var key = elements[i].getAttribute("data-tr");
        if (lang[lng][key]) {
            elements[i].innerHTML = lang[lng][key];
        }
    }
  }
  
